<?php

//perulangan for
echo "Perulangan For <br>";
for ($i = 0; $i < 5; $i++) {
    echo "Perulangan for ke - $i <br>";
}

//perulangan while
echo "<br>Perulangan While <br>";
$i = 0;
while ($i < 5) {
    echo "Perulangan while ke - $i <br>";
    $i++;
}

//perulangan do while
$i = 0;
echo "<br>Perulangan do while <br>";
do {
    echo "Perulangan do while ke - $i <br>";
    $i++;
} while ($i < 5);
